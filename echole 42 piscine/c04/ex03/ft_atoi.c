#include <stdio.h>

int	ft_atoi(char *str)
{
	int	result;
	int	sing;
	int	i;

	result = 0;
	sing = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-' )
	{
		if (str[i] == '-')
		{
			sing *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * sing);
}
